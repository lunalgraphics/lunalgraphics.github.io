<BlogPost
	title="LUNAL GRAPHICS IS GOING OPEN SOURCE"
	date="2026-05-22"
	description="The biggest news from Lunal Graphics in the last two years. Learn what this entails."
	{banner}
	bannerAlt="Shockwave"
	author="Yikuan Sun"
>
	<p>
		In the next few months, big changes will be coming to Lunal Graphics.
        In this post, I'll go over my rationale for these changes and what they mean for you.
	</p>

	<h2>Why am I doing this?</h2>
    <p>
        Since February of 2022, Lunal Graphics has been a major part of my life.
        I've spent a lot of time and energy not just building the apps themselves,
        but creating the content that goes along with them: art, tutorials, blog posts,
        this website, and more. I've also been responding to user feedback and help requests
        over social media and email.
    </p>
    <p>
        While I've always really enjoyed doing this, it has become unfeasible for me to do
        on a regular basis. Last fall, I started my first year of college at Harvard. Since then,
        I've been a full-time student, and I've also had to handle many other new responsibilities.
        Users have probably already noticed that I haven't been pushing updates, writing posts, or
        responding to messages in a timely manner.
    </p>
    <p>
        But I want Lunal Graphics to keep going strong. Artists all over the world use these tools,
        as reflected by our 162,000 and counting Photopea plugin installs. And none of the tools are
        perfect or finalized. I want them to keep improving and responding to the needs of the community.
    </p>
    <p>
        However, I can't do this alone. Thus, I have decided to open source the Lunal Graphics website
        and all of the apps. This will allow community members to contribute to the projects and improve
        Lunal Graphics in a fast-paced, collaborative way.
    </p>

    <h2>What does this mean for you?</h2>
    <p>
        If you're a developer, artist, or designer, you can contribute to the Lunal Graphics website and apps.
        You can help with documentation, fix bugs, add new features, and more.
        You can also help with the content that goes along with the apps (tutorials, art, website, etc.).
        This change will gradually be rolled out over the next few months.
    </p>
    <p>
        The Lunal Graphics apps will still maintain high quality, and they will be even better
        thanks to the contributions of the community.
    </p>
    <p>
        I also want to be in closer contact with enthusiastic contributors, possibly by putting together
        an official team to oversee development and customer support. If you're interested in this,
        please <a href="/contact">get in touch</a>.
    </p>

    <h2>What does this mean for me (Yikuan Sun)?</h2>
    <p>
        I'll still be around, but less frequently. I'll read and review your pull requests and issues,
        and I may even push new features occasionally. But much of the work will be done by you guys - 
        the community - from now on.
    </p>
    <p>
        Lunal Graphics will continue to be a significant part of my life, even if I'm around less often.
        I hope that through this change, Lunal Graphics will grow to become an even bigger part of your lives.
        I'm excited to see what the future holds for this project.
        Thank you so much for reading, and for being a part of this community!
    </p>
